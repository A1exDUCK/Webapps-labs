function combineChars(char1, char2, char3) {
    return char1 + char2 + char3;
}

console.log('Результат объединения символов:', combineChars('a', 'b', 'c')); // Выведет: abc
console.log('Результат объединения символов:', combineChars('%', '2', 'o')); // Выведет: %2o
console.log('Результат объединения символов:', combineChars('1', '5', 'p')); // Выведет: 15p
